            <p>Gym
                Diary（以下「本サービス」といいます）を運営する管理者（以下「運営者」といいます）は、利用者の個人情報を適切に取り扱い、保護することを重要な責務と考えています。本プライバシーポリシー（以下「本ポリシー」といいます）は、本サービスをご利用いただく際に収集される個人情報の取り扱いについて定めたものです。
