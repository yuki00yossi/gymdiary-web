            <h2 id="section1">第1条（適用範囲）</h2>
            <ol>
                <li>本ポリシーは、本サービスを通じて収集されるすべての個人情報に適用されます。</li>
                <li>本ポリシーは、利用者が本サービスを利用する際に同意いただいたものとみなします。</li>
                <li>本サービスが提供されるモバイルアプリおよびウェブサイトに適用されます。</li>
            </ol>

            <h2 id="section2">第2条（収集する情報）</h2>
            <p>運営者は、以下の情報を収集する場合があります。</p>
            <ul>
                <li>利用者が登録時に提供する情報（例: 氏名、メールアドレス、生年月日など）</li>
                <li>トレーニングや食事記録に関連する情報（例: 体重、食事内容、運動記録など）</li>
                <li>利用者の端末や接続に関する情報（例: IPアドレス、ブラウザ種類、OS情報、利用日時など）</li>
                <li>モバイルアプリにおいて、利用者が許可した場合に収集されるGPS情報や端末データ</li>
                <li>クッキーやトラッキングツールによって収集される情報（詳細は第9条を参照）</li>
            </ul>

            <h2 id="section3">第3条（情報の利用目的）</h2>
            <p>運営者は、収集した情報を以下の目的で利用します。</p>
            <ul>
                <li>本サービスの提供および運営</li>
                <li>利用者のトレーニング記録および進捗管理</li>
                <li>サービス向上のための分析および改善</li>
                <li>お問い合わせへの対応</li>
                <li>利用規約違反行為の防止および対策</li>
                <li>必要に応じた法令遵守および協力</li>
                <li>統計データの作成および研究目的での利用（個人を特定できない形式に限る）</li>
            </ul>

            <h2 id="section4">第4条（センシティブ情報の取り扱い）</h2>
            <ol>
                <li>運営者は、体重や健康に関する情報を利用者自身の進捗管理および統計的分析のために利用します。</li>
                <li>この情報は利用者の同意なしに第三者へ提供されることはありません。</li>
            </ol>

            <h2 id="section5">第5条（第三者提供の制限）</h2>
            <p>運営者は、以下の場合を除き、収集した情報を第三者に提供しません。</p>
            <ul>
                <li>利用者の同意がある場合</li>
                <li>法令に基づく場合</li>
                <li>人命または財産保護のために必要であり、利用者の同意を得ることが困難な場合</li>
                <li>公衆衛生の向上または児童の健全な育成推進のために特に必要な場合</li>
                <li>サービス運営のために外部委託業者を利用する場合（この場合、適切な管理を行います）</li>
            </ul>

            <h2 id="section6">第6条（情報の保存期間）</h2>
            <ol>
                <li>利用者の個人情報は、利用目的を達成するために必要な期間のみ保存します。</li>
                <li>アカウント削除後、収集された個人情報は30日以内に削除されます。ただし、法令に基づく場合や匿名化した統計データについては保存する場合があります。</li>
            </ol>

            <h2 id="section7">第7条（安全管理措置）</h2>
            <p>運営者は、利用者の個人情報を保護するため、以下の安全管理措置を講じます。</p>
            <ul>
                <li>情報への不正アクセスや漏えいを防ぐための技術的対策</li>
                <li>情報の取り扱いに関する従業者の教育および指導</li>
                <li>情報を保管するサーバーのセキュリティ強化</li>
                <li>データ漏洩や不正アクセスが確認された場合、速やかに影響を受ける利用者に通知し、必要な対策を講じます。</li>
            </ul>

